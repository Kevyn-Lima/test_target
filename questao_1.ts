// Declaração de variáveis com tipagem explícita
let INDICE: number = 13;
let SOMA: number = 0;
let K: number = 0;

// Loop que soma os números de 1 até o valor de INDICE
while (K < INDICE) {
    K += 1;  // Incrementa o valor de K
    SOMA += K;  // Adiciona o valor de K à variável SOMA
}

// Exibe o resultado da soma
console.log(SOMA);
